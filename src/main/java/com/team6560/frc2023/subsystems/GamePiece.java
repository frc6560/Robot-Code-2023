/**

The GamePiece enum represents the different types of game pieces that can be used in the game.
The NONE type represents no game piece, while the CONE and CUBE types represent different game pieces.
This enum can be used to keep track of the type of game piece being manipulated by the robot.
*/
package com.team6560.frc2023.subsystems;

public enum GamePiece {
    NONE, // Represents no game piece
    CONE, // Represents a cone game piece
    CUBE // Represents a cube game piece
}